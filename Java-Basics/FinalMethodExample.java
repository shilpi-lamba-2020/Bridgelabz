/*Java program that demonstrates the final method*/
public class FinalMethodExample {
	final void characteristics() {//method declared as final cannot be overridden
	int legs = 4;
	int ears = 2;
	int eyes = 2;
	int tail = 1;
	System.out.println("General Characteristics of an Animal are: ");
	System.out.println("Legs: " + legs);
	System.out.println("Eyes: " + eyes);
	System.out.println("Ears: " + ears);
	System.out.println("Tail: " + tail);
	}
}
	
